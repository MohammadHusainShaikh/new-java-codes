public class DataTypes {
    public static void main(String[] args) {
        int i = 84443;
        byte b = 127;
        short s = 5555;
        long l = 44433L;
        char c = 'C';
        float f = 8.4f;
        System.out.println(f);
        System.out.println(b);
        System.out.println(l);
        System.out.println(c);
        System.out.println(i);
        System.out.println(s);
    }
}
